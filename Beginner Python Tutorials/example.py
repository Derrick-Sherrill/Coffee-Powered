# list, tuple, dictionary, frozenset, set

our_list = ["a","b","c", 1.5]
print(our_list)

our_tuple = ("a","b","c")
print(our_tuple)

our_dictionary = {"name":"Derrick", "height":"6'0"}
print(our_dictionary)

our_set = {"one","two","three","two"}
print(our_set)

our_frozenset = frozenset(our_set)
print(our_frozenset)
